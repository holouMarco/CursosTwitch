

# Operadores relacionales
#  < > <= >= == !=


#Operador relacional <

numeroUno = 5
numeroDos = 5

print(numeroUno < numeroDos)

# Operador relacional >

print(numeroUno > numeroDos)

# Operador relacional <=

print(numeroUno <= numeroDos)

# Operador relacional >=

print(numeroUno >= numeroDos)

# Operador relacional ==

print(numeroUno == numeroDos)

# Operador relacional !=

print(numeroUno != numeroDos)



